package cn.p2p.p2p.prd.mobile.method.vo;

public class BorrowLoanVO {
	private String brand;
	private String buyMoney;
	private String assessMoney;
	private String verifyTimeStr;
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getBuyMoney() {
		return buyMoney;
	}
	public void setBuyMoney(String buyMoney) {
		this.buyMoney = buyMoney;
	}
	public String getAssessMoney() {
		return assessMoney;
	}
	public void setAssessMoney(String assessMoney) {
		this.assessMoney = assessMoney;
	}
	public String getVerifyTimeStr() {
		return verifyTimeStr;
	}
	public void setVerifyTimeStr(String verifyTimeStr) {
		this.verifyTimeStr = verifyTimeStr;
	}
	
}
